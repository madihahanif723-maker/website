var productMenu = document.getElementById("productMenu");
var brandMenu = document.getElementById("brandMenu");
var allProducts = document.getElementById("allProducts");
var productCategoryTitle = document.getElementById("productCategoryTitle");

if (productMenu && brandMenu && allProducts && productCategoryTitle) {
    window.renderProducts = function(category, brand) {
        allProducts.innerHTML = "";
        var categories = category ? [category] : Object.keys(products);

        categories.forEach(function (cat) {
            if (!products[cat]) return;
            Object.keys(products[cat]).forEach(function (brandKey) {
                if (brand && brand !== brandKey) return;
                var item = products[cat][brandKey];
                allProducts.innerHTML += `
        <div class="col-sm-6 col-md-4 col-lg-3 mb-4">
          <div class="card">
            <img src="${item.image}" onerror="this.onerror=null;this.src='https://via.placeholder.com/320x320?text=No+Image';" class="card-img-top" alt="${item.name}" />
            <div class="card-body">
              <h5 class="card-title">${item.name}</h5>
            </div>
            <div class="card-footer">
              <h5 class="price">Rs.${item.price}/-</h5>
            </div>
          </div>
        </div>`;
            });
        });

        if (allProducts.innerHTML === "") {
            allProducts.innerHTML = `<div class="col-12"><p class="text-muted">No products available.</p></div>`;
        }
    }

    window.fillCategoryOptions = function() {
        productMenu.innerHTML = `<option value="">Select Category</option>`;
        Object.keys(products).forEach(function (category) {
            productMenu.innerHTML += `
            <option value="${category}">${category.toUpperCase()}</option>`;
        });
    }

    window.fillBrandOptions = function(category) {
        brandMenu.innerHTML = `<option value="">Select Brand</option>`;
        if (!category || !products[category]) return;
        Object.keys(products[category]).forEach(function (brandKey) {
            brandMenu.innerHTML += `
                <option value="${brandKey}">${brandKey.toUpperCase()}</option>`;
        });
    }

    window.setCategoryTitle = function(category) {
        productCategoryTitle.textContent = category ? category.toUpperCase() : "All Products";
    }

    window.productChange = function() {
        var category = productMenu.value;
        setCategoryTitle(category);
        fillBrandOptions(category);
        renderProducts(category);
    }

    window.brandChange = function() {
        var category = productMenu.value;
        var brand = brandMenu.value;
        setCategoryTitle(category || "All Products");
        renderProducts(category || null, brand || null);
    }

    window.viewAll = function() {
        productMenu.value = "";
        brandMenu.value = "";
        setCategoryTitle("");
        fillBrandOptions("");
        renderProducts();
    }

    fillCategoryOptions();
    fillBrandOptions("");
    renderProducts();
}



function login() {
    var email = document.getElementById("login-email").value.trim();
    var password = document.getElementById("login-password").value.trim();
    var userName = document.getElementById("name").value.trim();

    if (email && password && userName) {
        var loginStatus = true;
        var currentUser = userName.charAt(0).toUpperCase() + userName.slice(1);
        Swal.fire({
            icon: "success",
            title: "Login Successful",
            text: "Welcome! " + currentUser
        }).then(() => {
            window.location.href = 'index.html';
        });

    } else {
        Swal.fire({
            icon: "error",
            title: "Fill all fields",
        });

    }
}

